import java.util.*;
class Inputs
{
  public static void main(String[] args)
  {
    Scanner sc= new Scanner(System.in);
    System.out.println("Enter float");
    float f =sc.nextFloat();
    System.out.println("Your float= "+f);
    System.out.println("Enter Double");
    double d=sc.nextDouble();
    System.out.println("Your double="+d);
    System.out.println("Enter string=");
    String s=sc.next();
    System.out.println("Your string="+s);
    System.out.println("Enter Boolean");
    boolean b=sc.nextBoolean();
    System.out.println("Your Boolean="+b);
    System.out.println("Enter long=");
    long l=sc.nextLong();
    System.out.println("Your long="+l);
    System.out.println("Enter short=");
    short sh=sc.nextShort();
    System.out.println("Your short="+sh);
    System.out.println("Enter byte=");
    byte by=sc.nextByte();
    System.out.println("Your byte="+by);
    
   }
}